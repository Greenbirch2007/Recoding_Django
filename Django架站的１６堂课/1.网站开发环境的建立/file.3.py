#  步骤２　　切换到项目目录下
#  步骤３　使用git push 从远程文档库拉取最新版本的网站资料到本地文档库
#  步骤４　使用  piip install -r "requirements.txt" ,安装所有使用到的套件
#  步骤５　开始编辑工作
#  步骤６　测试完毕，要结束此计算机的操作时，在使用pip freeze > requirements.txt确定当前使用到的所有套件
#  步骤７　使用git push 上传所有的更新操作